let A = 3.0
let B = 4.0
let C = 5.2
const pi = 3.14159;
let areaTriRetang = (A * C) / 2;
let areaCirculo =  pi * C ** 2;
let areaTrapezio = ((A + B) * C) / 2;
let areaQuadrado = B ** 2;
let areaRetangulo = A * B;
console.log(`TRIANGULO: ${areaTriRetang.toFixed(3)}
CIRCULO: ${areaCirculo.toFixed(3)}
TRAPEZIO: ${areaTrapezio.toFixed(3)}
QUADRADO: ${areaQuadrado.toFixed(3)}
RETANGULO: ${areaRetangulo.toFixed(3)}`);